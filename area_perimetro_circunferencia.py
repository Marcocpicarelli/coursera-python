# Programa para a calcular a área de um círculo e o
# perímetro de uma circunferência

def area_perimetro_circunf(r):

    pi = 3.14
    pi = float(pi)
    area = pi*(r**2)
    perimetro = 2*pi*r

    print("A área do seu círculo é " + str(area) + ".")
    print("O perímetro da sua circunferência e " + str(perimetro) + ".")



